function hnd_ut(a) {
  a.TRANSLATIONS["Search term too short"] = "Búsqueda demasiado corta";
  a.TRANSLATIONS["No results"] = "Sin resultados";
  a.TRANSLATIONS["Please enter more characters"] =
    "Por favor, introduzca más caracteres";
  a.TRANSLATIONS[
    "Word list not ready yet. Please wait until the word list is fully downloaded"
  ] =
    "La lista todavía no se ha llenado. Por favor esperé hasta que haya completado su descarga";
  a.TRANSLATIONS[
    "Incorrect or corrupt search data. Please check your HelpNDoc template"
  ] = "Búsqueda incorrecta o corrupta. Por favor revise su plantilla HelpNDoc";
  a.TRANSLATIONS["Related topics..."] = "Términos relacionados...";
  a.TRANSLATIONS["Loading..."] = "Cargando...";
  a.TRANSLATIONS["Close"] = "Cerrar";
}
